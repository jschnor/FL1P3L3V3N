                <h2>Our Recommendation</h2>
                <p>Flipeleven proposes a three-phase process to launch 2C Media's new website. It will start with a collaboration on the website strategy. Then move into Phase II where Flipeleven will prototype the website while creating design concepts as 2C Media collects and creates content. The final phase will wrap with Flipeleven's development team combining content, designs and prototypes to a staging link for quality assurance testing before the final website launch.</p>
