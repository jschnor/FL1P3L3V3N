                <h2 class="subheader">Project Summary</h2>
                <h5 class="subheader">Website Strategy, Design & Development</h5>
                <p>Founded by Chris and Carla Sloan, 2C Media is a passion-driven creative production company that creates award winning work – earning them over 12 PromaxBDA Awards in the past five years. The Miami-based creative company has built their business on creating promotions and original-content.</p>

                <p>Currently, 2C Media is going through a rebrand that will include a redesign of their existing website. They desire a website that competes with similar creative production companies, but separates 2C by highlighting both why they do what they do as well as what they do. They also request that the technology for the new site be more future-proof than the previous flash-based website.</p>
                <p>2C Media has engaged with Flipeleven Creative to design, develop and guide 2C Media on the creation of their new website. To date, Flipeleven and 2C Media have exchanged e-mails and conference calls to discuss the details of the project.</p>
