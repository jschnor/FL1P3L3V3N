                <h3>Phase I</h3>
                <h4>Website Strategy</h4>
                <p>Flipelven would lead brainstorms, discussions and/or in-person meetings with 2C Media to both determine a content structure and ideate over a creative direction for the website. This is a foundational step in determining a technical solution that will remain valuable over time. This collaboration helps us to determine exact project scope for the following two phases.</p>
